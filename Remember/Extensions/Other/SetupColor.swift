import UIKit

class SetupColor {
    static func white() -> UIColor {
        return UIColor(hex: "FFFAEE")
    }
    
    static func blue() -> UIColor {
        return UIColor(hex: "102A39")
    }
    
    static func turquoise() -> UIColor {
        return UIColor(hex: "3E959D")
    }
    
    static func orange() -> UIColor {
        return UIColor(hex: "E5986D")
    }
    
    static func red() -> UIColor {
        return UIColor(hex: "E33357")
    }
}
